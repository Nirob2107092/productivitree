import SwiftUI

enum Theme {
    enum Colors {
        static let background = Color(.systemGroupedBackground)
        static let surface = Color(.secondarySystemGroupedBackground)
        static let surfaceAlt = Color(.systemGray5)
        static let stroke = Color(.systemGray4)
        static let accent = Color(red: 0.12, green: 0.62, blue: 0.37)
        static let accentSecondary = Color(red: 0.09, green: 0.55, blue: 0.80)
        static let warning = Color.orange
    }
}
